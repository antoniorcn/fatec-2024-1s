public class Contato { 
    private String nome;
    private String tel;

    public String getNome() { 
        return this.nome;
    }
    public void setNome(String valor) { 
        this.nome = valor;
    }

    public String getTel() { 
        return this.tel;
    }

    public void setTel(String valor) { 
        this.tel = valor;
    }

}